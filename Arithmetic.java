public class Arithmetic {
	public static void main(String[] args)
	{
//		double a = 7, b = 2;
		int a = 7, b = 2;
		System.out.println("a + b = " + (a + b));
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + (a / b));
		
		System.out.println("a + b / 2 = " + (a + b / 2));
		System.out.println("(a + b) /2 = " + ((a + b) / 2));

		System.out.println("a % b = " + (a % b));
		System.out.println("sqrt(a + b) = " + Math.sqrt(a + b));
		
		
	}
}
